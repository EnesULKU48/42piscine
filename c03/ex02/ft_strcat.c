char	*ft_strcat(char *dest, char *src)
{
	int	s;
	int	d;

	d = 0;
	while (dest[d])
	{
		d++;
	}
	s = 0;
	while (src[s])
	{
		dest[d] = src[s];
		d++;
		s++;
	}
	dest[d] = '\0';
	return (dest);
}
