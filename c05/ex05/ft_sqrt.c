int	ft_sqrt(int nb)
{
	long	i;
	long	number;

	number = nb;
	i = 2;
	if (nb == 1)
		return (1);
	if (nb >= 2)
	{
		while (i * i <= number)
		{
			if (i * i == number)
				return (i);
			i++;
		}
	}
	return (0);
}
